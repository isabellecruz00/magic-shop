class Usuario {
    constructor(nome, cpf, endereco, cidade, estado, pais, email, nome_usuario, senha) {
      this.nome = nome;
      this.cpf = cpf;
      this.endereco = endereco;
      this.cidade = cidade;
      this.estado = estado;
      this.pais = pais;
      this.email = email;
      this.nome_usuario = nome_usuario;
      this.senha = senha;
    }
   }
   
   module.exports = Usuario;
   